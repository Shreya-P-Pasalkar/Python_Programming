class Demo :
    Value = 10      # Class Variable

    ##########################################################################################################
    #   Function Name : __init__
    #   Function Type : special Function
    #   Description :   Constructor
    #   Author :        Shreya Pramod Pasalkar
    #   Date :          28/01/2026
    ##########################################################################################################
    def __init__(self,No1,No2) :
        self.Value1 = No1
        self.Value2 = No2

    ###########################################################################################################
    #   Function Name : fun
    #   Function Type : User Defined Function
    #   Description :   It is used to print instance variables
    #   Author :        Shreya Pramod Pasalkar
    #   Date :          28/01/2026
    ###########################################################################################################
    def fun(self) :
        print("Inside fun")
        print("No1 : ",self.Value1)
        print("No2 : ",self.Value2)

    ###########################################################################################################
    #   Function Name : gun
    #   Function Type : User Defined Function
    #   Description :   It is used to print instance variables
    #   Author :        Shreya Pramod Pasalkar
    #   Date :          28/01/2026
    ###########################################################################################################
    def gun(self) :
        print("Inside gun")
        print("No1 : ",self.Value1)
        print("No2 :",self.Value2)

##############################################################################################################
#   Object Creation
##############################################################################################################
obj1 = Demo(11,21)
obj2 = Demo(51,101)

##############################################################################################################
#   Function Calls
##############################################################################################################
obj1.fun()
obj2.fun()
obj1.gun()
obj2.gun()