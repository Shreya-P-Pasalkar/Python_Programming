##############################################################################################################
#   Function Name : Power
#   Function Type : Lambda Function
#   Description :   It is used to calculate the power of 2 of the given number
#   Author :        Shreya Pramod Pasalkar
#   Date :          23/01/2026
##############################################################################################################
Power = lambda No : No**2

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Value = 0
    Ret = 0

    print("Enter Number : ")
    Value = int(input())

    Ret = Power(Value)

    print("Power of ",Value," : ",Ret)

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()
