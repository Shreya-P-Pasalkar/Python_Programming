##############################################################################################################
#   Required Modules
##############################################################################################################
import threading

##############################################################################################################
#   Function Name : Display
#   Function Type : User Defined Function
#   Description :   It is used to display numbers 1-50
#   Author :        Shreya Pramod Pasalkar
#   Date :          23/01/2026
##############################################################################################################
def Display() :
    i = 0

    for i in range(1,(50+1)) :
        print(i,end = "\t")

    print("")
    print("----------------------------------------------------------------------------------------------")

##############################################################################################################
#   Function Name : ChkCapital
#   Function Type : User Defined Function
#   Description :   It is used to display 1-50 in reverse order
#   Author :        Shreya Pramod Pasalkar
#   Date :          23/01/2026
##############################################################################################################
def DisplayReverse() :
    i = 0

    for i in range(50,0,-1) :
        print(i,end = "\t")

    print("")
    print("----------------------------------------------------------------------------------------------")

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    t1 = threading.Thread(target = Display)
    t2 = threading.Thread(target = DisplayReverse)

    t1.start()
    t1.join()

    t2.start()
    t2.join()
    
###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()
