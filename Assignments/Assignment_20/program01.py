import threading

##############################################################################################################
#   Function Name : PrintEven
#   Function Type : User Defined Function
#   Description :   It is used to print even numbers
#   Author :        Shreya Pramod Pasalkar
#   Date :          23/01/2026
##############################################################################################################
def PrintEven() :
    i = 1
    No = 10*2

    print("Even numbers : ")
    while(No != 0) :
        if(i%2 == 0) :
            print(i, end = "\t")
        i = i + 1
        No = No-1
        
    print("")

##############################################################################################################
#   Function Name : PrintOdd
#   Function Type : User Defined Function
#   Description :   It is used to print Odd numbers
#   Author :        Shreya Pramod Pasalkar
#   Date :          23/01/2026
##############################################################################################################
def PrintOdd() :
    i = 1
    No = 10*2

    print("Odd numbers : ")
    while(No != 0) :
        if(i%2 != 0) :
            print(i, end = "\t")
        i = i + 1
        No = No-1
        
    print("")   

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Even = threading.Thread(target = PrintEven)
    Odd = threading.Thread(target = PrintOdd)

    Even.start()
    Odd.start()

    Even.join()
    Odd.join()
    
###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()
