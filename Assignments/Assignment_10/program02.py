def SumNatural(No):
    i = 0
    Sum = 0

    for i in range(1,(No+1)) :
        Sum = Sum + i

    return Sum

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Ret = 0

    print("Enter number :")
    Value = int(input())

    Ret = SumNatural(Value)

    print("Summation is : ",Ret)

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()