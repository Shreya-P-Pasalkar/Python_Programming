def Factorial(No):
    i = 0
    Fact = 1

    for i in range(1,(No+1)) :
        Fact = Fact * i

    return Fact

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Ret = 0

    print("Enter number :")
    Value = int(input())

    Ret = Factorial(Value)

    print("Factorial is : ",Ret)

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()