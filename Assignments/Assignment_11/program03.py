def DigitSum(No):
    Sum = 0
    Digit = 0

    while (No != 0):
        Digit = No%10
        Sum = Sum + Digit
        No = No/10

    return Sum

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Ret = 0

    print("Enter number :")
    Value = int(input())

    Ret = DigitSum(Value)

    print("Sum of digits : ",Ret)

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()