def ChkPrime(No):
    i = 0
    Sum = 0

    for i in range(1,(No/2)) :
        if(No%i == 0):
            Sum = Sum+i

    if (Sum == 1) :
        return True
    else :
        return False

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Ret = 0

    print("Enter number :")
    Value = int(input())

    Ret = ChkPrime(Value)

    if (Ret == True) :
        print(Value," is Prime")
    else :
        print(Value," is not Prime")

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()