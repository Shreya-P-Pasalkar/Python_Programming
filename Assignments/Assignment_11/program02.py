def CountDigit(No):
    Sum = 0

    while (No != 0):
        Sum = Sum + 1
        No = No/10

    return Sum

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Ret = 0

    print("Enter number :")
    Value = int(input())

    Ret = CountDigit(Value)

    print("Number of digits : ",Ret)

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()