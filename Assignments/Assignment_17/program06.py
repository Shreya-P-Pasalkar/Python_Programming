def Display(No) :
    i = 0
    
    for i in range(No,0,-1) :
        print("*\t" * i)

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Value = 0

    print("Enter Number : ")
    Value = int(input())

    Display(Value)

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()

