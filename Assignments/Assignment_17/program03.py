def Factorial(No) :
    i = 0
    Fact = 1

    for i in range(No,0,-1) :
        Fact = Fact * i
    
    return Fact

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Value = 0

    print("Enter Number : ")
    Value = int(input())

    Ret = Factorial(Value)

    print("Factorial is : ",Ret)

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()