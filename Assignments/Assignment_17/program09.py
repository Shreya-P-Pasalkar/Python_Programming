def Digits(No) :
    i = 0
    iCount = 0
    No = str(No)
    
    for i in No :
        iCount = iCount + 1

    return iCount

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Value = 0
    Ret = 0

    print("Enter Number : ")
    Value = int(input())

    Ret = Digits(Value)

    print("Number of Digits : ",Ret)

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()
