from functools import reduce

##############################################################################################################
#   Function Name : Addition
#   Function Type : Lambda Function
#   Description :   It is used to addition
#   Author :        Shreya Pramod Pasalkar
#   Date :          20/01/2026
##############################################################################################################
Addition = lambda No1,No2 : No1+No2

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Data = list()
    iSize = 0

    print("Enter number of elements you want in you list : ")
    iSize = int(input())

    for i in range(1,(iSize+1)) :
        Value = int(input())
        Data.append(Value)

    RData = reduce(Addition,Data)
    print("Reduces Data(Addition of all elements) is : ",RData)

###############################################################################################################
#   Starter
##################c############################################################################################
if __name__ == "__main__" :
    main()