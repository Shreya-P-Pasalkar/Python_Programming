import os

##########################################################################################################
#   Function Name : ChkExistence
#   Function Type : User-defined Function
#   Description :   Used to check if the give file exists or not
#   Author :        Shreya Pramod Pasalkar
#   Date :          04/02/2026
##########################################################################################################
def ChkExistence(FileName) :
    return os.path.exists(FileName)
    
##########################################################################################################
# Entry Point Function (User defined)
##########################################################################################################
def main() :
    FileName = input("Enter name of file :")

    Ret = ChkExistence(FileName)

    if(Ret == True) :
        print(f"{FileName} exists")
    else :
        print(f"{FileName} doesn't exists")

##########################################################################################################
# Starter
##########################################################################################################
if __name__ == "__main__" :
    main()