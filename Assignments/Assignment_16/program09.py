def DisplayEven() :
    i = 0
    
    for i in range (1,(2*10 + 1)) :
        if(i%2 == 0) :
            print(i,"\t")

##############################################################################################################
#   Entry Point Function
##############################################################################################################  
def main() :
    DisplayEven()

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()