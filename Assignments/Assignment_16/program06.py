def ChkNumber(No) :
    if (No < 0) :
        print("Negative Number")
    elif (No > 0) :
        print("Positive Number")
    elif (No == 0) :
        print("Zero")

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Value = 0

    print("Enter number : ")
    Value = int(input())

    ChkNumber(Value)    

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()