def ChkEven(No) :
    if(No%2 == 0) :
        return True
    else :
        return False

##############################################################################################################
#   Entry Point Function
##############################################################################################################    
def main() :
    Ret = False

    print("Enter number")
    Value = int(input())

    Ret = ChkEven(Value)

    if(Ret == True) :
        print("Even Number")
    else :
        print("Odd Number")

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()