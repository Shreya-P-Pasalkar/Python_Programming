def CountLength(Word) :
    iCount = 0
    
    for i in Word :
        iCount = iCount + 1

    return iCount
      
##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Ret = 0

    print("Enter Name : ")
    Name = input()

    Ret = CountLength(Name)

    print("Length of the string is : ",Ret)

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()