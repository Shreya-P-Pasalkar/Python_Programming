Add = lambda No1,No2 : No1+No2

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Value1 = 0
    Value2 = 0
    Ret = 0

    print("Enter first Number : ")
    Value1 = int(input())

    print("Enter Second Number : ")
    Value2 = int(input())

    Ret = Add(Value1,Value2)

    print("Addition is : ",Ret)

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()