def ChkDivisibility(No) :
    return (No%5 == 0)

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main() :
    Value = 0
    Ret = False

    print("Enter number : ")
    Value = int(input())

    Ret = ChkDivisibility(Value)  

    print("Number is divisible by 5 : ",Ret)

###############################################################################################################
#   Starter
###############################################################################################################
if __name__ == "__main__" :
    main()