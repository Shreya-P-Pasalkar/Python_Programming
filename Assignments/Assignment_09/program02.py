def ChkGreater(No1,No2):
    Max = 0.0

    if No1 > No2 :
        Max = No1
    else :
        Max = No2
    
    return Max

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Ans = 0.0

    print("Enter first number :")
    Value1 = int(input())

    print("Enter second number :")
    Value2 = int(input())

    Ans = ChkGreater(Value1,Value2)

    print("Greater number is : ",Ans)

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()