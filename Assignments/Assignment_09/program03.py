def Square(No):
    square = No * No

    return square

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Ans = 0.0

    print("Enter first number :")
    Value = int(input())

    Ans = Square(Value)

    print("Square of given number is : ",Ans)

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()