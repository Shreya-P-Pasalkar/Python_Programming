import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

#################################################################################################
#   Function Name : Plotting
#   Usage : To plot the ScatterPlot
#   Date : 1/03/2026
#   Author : Shreya Pramod Pasalkar
#################################################################################################
def Plotting(df) :
        
    sns.scatterplot(data= df,x = df['StudyHours'],y = df['PreviousScore'], hue = df['FinalResult'], palette= {0 : 'red', 1 : 'skyblue'})

    plt.title("StudyHours vs PreviousScore",fontsize=15)
    plt.xlabel("StudyHours",fontsize= 12)
    plt.ylabel("PreviousScore", fontsize = 12)
    plt.legend(title="Outcome")
    plt.show()

#################################################################################################
#   Entry Point Function
#################################################################################################
def main() :
    Border = "-"*100 
    print(Border)

    #############################################################################################
    #   Load the Dataset
    #############################################################################################
    Dataset = "student_performance_ml.csv"

    df = pd.read_csv(Dataset)

    print("Dataset loaded sucessfully!")

    print(Border)

    #############################################################################################
    #  Plot the graphs
    #############################################################################################
    Plotting(df)

#################################################################################################
#   Starter
#################################################################################################
if __name__ == "__main__" :
    main()

