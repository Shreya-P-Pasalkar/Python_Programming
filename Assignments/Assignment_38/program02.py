import pandas as pd

#################################################################################################
#   Function Name : Display
#   Usage : To display information about the Dataset
#   Date : 1/03/2026
#   Author : Shreya Pramod Pasalkar
#################################################################################################
def Display(df) :

    Border = "-" * 100

    print("Total Number of Students : \n")
    print(len(df.index))
    print(Border)

    Passed = (df['FinalResult'] == '1').sum()
    Failed = (df['FinalResult'] == '0').sum()

    print(f"Number of Students Passed : {Passed}\n")
    print(f"Number of Students Failed : {Failed}\n")
    print(Border)

#################################################################################################
#   Entry Point Function
#################################################################################################
def main() :
    Border = "-"*100
    print(Border)

    #############################################################################################
    #   Load the Dataset
    #############################################################################################
    Dataset = "student_performance_ml.csv"

    df = pd.read_csv(Dataset)

    print("Dataset loaded sucessfully!")

    print(Border)

    #############################################################################################
    #   Display 
    #############################################################################################
    Display(df)

#################################################################################################
#   Starter
#################################################################################################
if __name__ == "__main__" :
    main()

