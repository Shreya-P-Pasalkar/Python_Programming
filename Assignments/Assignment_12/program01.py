def ChkVowel(Ch):
    if (Ch == 'a' or Ch == 'e' or Ch =='o' or Ch == 'i' or Ch == 'u'):
        return True
    else :
        return False

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Ret = 0

    print("Enter character :")
    Value = str(input())

    Ret = ChkVowel(Value)

    if(Ret == True):
        print("It is Vowel")
    else :
        print("It is not a Vowel")

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()