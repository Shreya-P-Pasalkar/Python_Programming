def PrintReverseNumbers(No) :
    i = 0

    for i in range (No,0,-1):
        print(i,"\t")

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    print("Enter number :")
    Value = int(input())

    PrintReverseNumbers(Value)

##############################################################################################################
#   Starter
##################c############################################################################################
if __name__ == "__main__" :
    main()