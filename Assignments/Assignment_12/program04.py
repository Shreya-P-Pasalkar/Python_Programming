def PrintNumbers(No) :
    i = 0

    for i in range (1,(No+1)):
        print(i,"\t")

##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    print("Enter number :")
    Value = int(input())

    PrintNumbers(Value)

##############################################################################################################
#   Starter
##############################################################################################################
if __name__ == "__main__" :
    main()