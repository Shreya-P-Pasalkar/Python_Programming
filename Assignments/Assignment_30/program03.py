import os
import sys

##########################################################################################################
#   Function Name : DisplayLines
#   Function Type : User-defined Function
#   Description :   Used to display lines from the file
#   Author :        Shreya Pramod Pasalkar
#   Date :          04/02/2026
##########################################################################################################
def DisplayLines(File) :

    if(os.path.exists(File) == False) :
        print(f"{File} doesn't exists")
        return
    
    fobj = open(File,"r")

    for line in fobj :
        Updated = line.split(".") 
        print(Updated)

    fobj.close()
    
##########################################################################################################
# Entry Point Function (User defined)
##########################################################################################################
def main() :
    DisplayLines(sys.argv[1])

##########################################################################################################
# Starter
##########################################################################################################
if __name__ == "__main__" :
    main()