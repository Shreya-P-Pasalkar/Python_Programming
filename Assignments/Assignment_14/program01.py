Square = lambda No : (No*No)
 
##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Value = 0
    Ret = 0

    print("Enter Number :")
    Value = int(input())

    Ret = Square(Value)

    print("Square of",Value," is :",Ret)

##############################################################################################################
#   Starter
##################c############################################################################################
if __name__ == "__main__" :
    main()