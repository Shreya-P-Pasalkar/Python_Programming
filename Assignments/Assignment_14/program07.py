Divisibility = lambda No : ((No % 5) == 0)
 
##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Value = 0
    Ret = False

    print("Enter Number :")
    Value = int(input())

    Ret = Divisibility(Value)

    print(Value," is divisible by 5 :",Ret)

##############################################################################################################
#   Starter
##################c############################################################################################
if __name__ == "__main__" :
    main()