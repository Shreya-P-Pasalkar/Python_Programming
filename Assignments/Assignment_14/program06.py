##############################################################################################################
#   Function Name : ChkOdd
#   Function Type : Lambda Function
#   Description :   It is used to check if the given number is odd or not
#   Author :        Shreya Pramod Pasalkar
#   Date :          20/01/2026
##############################################################################################################
ChkOdd = lambda No : ((No % 2) != 0)
 
##############################################################################################################
#   Entry Point Function
##############################################################################################################
def main():
    Value = 0
    Ret = False

    print("Enter Number :")
    Value = int(input())

    Ret = ChkOdd(Value)

    print(Value," is Odd :",Ret)

##############################################################################################################
#   Starter
##################c############################################################################################
if __name__ == "__main__" :
    main()